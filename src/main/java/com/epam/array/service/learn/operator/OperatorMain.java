package com.epam.array.service.learn.operator;

public class OperatorMain {
  public static void main(String[] args) {
    var number = 5.0d;
    System.out.println(number + 1 > 5.0);
    System.out.println(number >= 5.0);
    System.out.println(number < 5 + 2.0);
    System.out.println(number <= 5);
    System.out.println(number * 2 != 5);
    System.out.println(number == 5);
  }
}
